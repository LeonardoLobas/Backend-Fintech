CREATE TABLE T_TIPO_INVESTIMENTO (
    ID_TIPO_INVESTIMENTO   NUMBER GENERATED BY DEFAULT AS IDENTITY,
    TIPO_INVESTIMENTO      VARCHAR2(100) NOT NULL,
